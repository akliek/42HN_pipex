#include "pipex.h"

void	clean_str(char *s)
{
	free(s);
	s = NULL;
}

void	clean_str_arr(char **s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		clean_str(s[i]);
		i++;
	}
}
